#include "node.h"


////////////////////////////////////////////////////////////////////////////////
Node::Node() {}


////////////////////////////////////////////////////////////////////////////////
Node::Node(string b, uint t, State s){
	pos = b;
	time = t;
	state = s;
}


////////////////////////////////////////////////////////////////////////////////
void Node::addInNode(Node *node){
	inNodes_.push_back(node);
}

////////////////////////////////////////////////////////////////////////////////
void Node::addOutNode(Node *node){
	outNodes_.push_back(node);
}

////////////////////////////////////////////////////////////////////////////////
vector<Node*> Node::getOutNode(){
	return outNodes_;
}


////////////////////////////////////////////////////////////////////////////////
State Node::getState(){
	return state;
}

////////////////////////////////////////////////////////////////////////////////
string Node::getPosition(){
	return pos;
}

////////////////////////////////////////////////////////////////////////////////
uint Node::getTime(){
	return time;
}

////////////////////////////////////////////////////////////////////////////////
Node& Node::operator=(Node &n){
	pos = n.pos;
	time = n.time;
	return *this;
}